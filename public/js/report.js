// js/report.js — Reports controller
import { apiFetch, getSession } from './api.js';

export async function init() {
  const reportType = document.getElementById('report-type');
  const bfGroup = document.getElementById('sub-filter-beneficiary-group');
  const appGroup = document.getElementById('sub-filter-application-group');
  
  const bfCategory = document.getElementById('report-beneficiary-category');
  const bfStatus = document.getElementById('report-beneficiary-status');
  
  const appProgram = document.getElementById('report-application-program');
  const appStatus = document.getElementById('report-application-status');
  
  const btnGenerate = document.getElementById('btn-generate-report');
  const btnPrint = document.getElementById('btn-print-report');
  
  const titleDisplay = document.getElementById('report-title-display');
  const metaDisplay = document.getElementById('report-meta-display');
  const headerRow = document.getElementById('report-table-header');
  const body = document.getElementById('report-table-body');
  const signoffSection = document.getElementById('report-signoff');
  const signoffOfficer = document.getElementById('signoff-officer');
  const emptyState = document.getElementById('report-empty-state');
  const printContainer = document.getElementById('report-print-container');

  const session = getSession();

  // 1. Populated Programs dropdown for Applications report
  async function loadAppPrograms() {
    try {
      const programs = await apiFetch('/programs');
      appProgram.innerHTML = '<option value="All">All Programs</option>';
      programs.forEach(p => {
        const opt = document.createElement('option');
        opt.value = p.id;
        opt.textContent = `${p.program_code} - ${p.name}`;
        appProgram.appendChild(opt);
      });
    } catch (e) {
      console.warn('Failed to load programs list for reports:', e);
    }
  }

  // Helper to reset report card and print button visibility
  function resetReportView() {
    emptyState.classList.remove('d-none');
    printContainer.classList.add('d-none');
    btnPrint.classList.add('d-none');
    signoffSection.classList.add('d-none');
    body.innerHTML = '';
    headerRow.innerHTML = '';
  }

  // 2. Manage Sub-Filters Visibility
  reportType.addEventListener('change', () => {
    resetReportView();
    const val = reportType.value;
    if (val === 'beneficiaries') {
      bfGroup.classList.remove('d-none');
      appGroup.classList.add('d-none');
    } else if (val === 'applications') {
      bfGroup.classList.add('d-none');
      appGroup.classList.remove('d-none');
      loadAppPrograms();
    } else {
      bfGroup.classList.add('d-none');
      appGroup.classList.add('d-none');
    }
  });

  bfCategory.addEventListener('change', resetReportView);
  bfStatus.addEventListener('change', resetReportView);
  appProgram.addEventListener('change', resetReportView);
  appStatus.addEventListener('change', resetReportView);

  // 3. Generate Report handler
  btnGenerate.addEventListener('click', async () => {
    const type = reportType.value;

    // Hide print button while compiling
    btnPrint.classList.add('d-none');
    
    // Show print container, hide empty state
    emptyState.classList.add('d-none');
    printContainer.classList.remove('d-none');

    body.innerHTML = `<tr><td class="text-muted text-center" style="padding: 40px 0;">Compiling report ledger...</td></tr>`;
    
    // Set Metadata
    const userName = session ? session.user.full_name : 'Staff Officer';
    const dateStr = new Date().toLocaleDateString();
    metaDisplay.textContent = `Generated on: ${dateStr} | Generated by: ${userName}`;
    signoffOfficer.textContent = userName;
    signoffSection.classList.remove('d-none');

    try {
      if (type === 'beneficiaries') {
        titleDisplay.textContent = 'BENEFICIARY DIRECTORY LEDGER';
        
        let url = `/reports/beneficiaries?`;
        if (bfCategory.value !== 'All') url += `category=${encodeURIComponent(bfCategory.value)}&`;
        if (bfStatus.value !== 'All') url += `status=${encodeURIComponent(bfStatus.value)}&`;
        
        const data = await apiFetch(url);
        renderBeneficiaryReport(data);
      } else if (type === 'applications') {
        titleDisplay.textContent = 'APPLICATION STATUS SUMMARY';
        
        let url = `/reports/applications?`;
        if (appProgram.value !== 'All') url += `program_id=${encodeURIComponent(appProgram.value)}&`;
        if (appStatus.value !== 'All') url += `status=${encodeURIComponent(appStatus.value)}&`;
        
        const data = await apiFetch(url);
        renderApplicationReport(data);
      } else if (type === 'programs') {
        titleDisplay.textContent = 'PROGRAM CAPACITY & UTILIZATION SUMMARY';
        const data = await apiFetch('/reports/programs');
        renderProgramReport(data);
      }
      
      // Successfully generated, show print button!
      btnPrint.classList.remove('d-none');
    } catch (err) {
      body.innerHTML = `<tr><td class="text-muted text-center" style="padding: 40px 0; color: var(--red);">Error generating report: ${err.message}</td></tr>`;
      btnPrint.classList.add('d-none');
    }
  });

  // Render Beneficiaries report list
  function renderBeneficiaryReport(records) {
    headerRow.innerHTML = `
      <tr>
        <th>Code</th>
        <th>Full Name</th>
        <th>Address</th>
        <th>Contact</th>
        <th>Category</th>
        <th>Status</th>
      </tr>
    `;
    
    if (records.length === 0) {
      body.innerHTML = `<tr><td colspan="6" class="text-muted text-center" style="padding: 40px 0;">No matching beneficiary records found.</td></tr>`;
      return;
    }

    body.innerHTML = '';
    records.forEach(row => {
      const tr = document.createElement('tr');
      tr.innerHTML = `
        <td><strong>${escapeHtml(row.beneficiary_code)}</strong></td>
        <td>${escapeHtml(row.full_name)}</td>
        <td>${escapeHtml(row.address)}</td>
        <td>${escapeHtml(row.contact_number)}</td>
        <td><span class="badge badge-staff">${escapeHtml(row.assistance_category)}</span></td>
        <td><span class="badge ${row.status === 'active' ? 'badge-active' : 'badge-closed'}">${escapeHtml(row.status)}</span></td>
      `;
      body.appendChild(tr);
    });
  }

  // Render Applications report list
  function renderApplicationReport(records) {
    headerRow.innerHTML = `
      <tr>
        <th>App Code</th>
        <th>Beneficiary Name</th>
        <th>Program</th>
        <th>Applied Date</th>
        <th>Status</th>
        <th>Claim Code</th>
      </tr>
    `;
    
    if (records.length === 0) {
      body.innerHTML = `<tr><td colspan="6" class="text-muted text-center" style="padding: 40px 0;">No matching application records found.</td></tr>`;
      return;
    }

    body.innerHTML = '';
    records.forEach(row => {
      const tr = document.createElement('tr');
      const date = new Date(row.applied_at).toLocaleDateString();
      let badgeClass = 'badge-pending';
      if (row.status === 'approved') badgeClass = 'badge-approved';
      else if (row.status === 'rejected') badgeClass = 'badge-rejected';
      else if (row.status === 'released') badgeClass = 'badge-released';

      tr.innerHTML = `
        <td><strong>${escapeHtml(row.application_code)}</strong></td>
        <td>${escapeHtml(row.beneficiary_name)}</td>
        <td>${escapeHtml(row.program_name)}</td>
        <td>${date}</td>
        <td><span class="badge ${badgeClass}">${escapeHtml(row.status)}</span></td>
        <td><code>${escapeHtml(row.claim_code || '—')}</code></td>
      `;
      body.appendChild(tr);
    });
  }

  // Render Programs report list
  function renderProgramReport(records) {
    headerRow.innerHTML = `
      <tr>
        <th>Prog Code</th>
        <th>Program Name</th>
        <th>Category</th>
        <th>Budget Slots</th>
        <th>Total Apps</th>
        <th>Approved</th>
        <th>Released</th>
      </tr>
    `;
    
    if (records.length === 0) {
      body.innerHTML = `<tr><td colspan="7" class="text-muted text-center" style="padding: 40px 0;">No programs registered.</td></tr>`;
      return;
    }

    body.innerHTML = '';
    records.forEach(row => {
      const tr = document.createElement('tr');
      tr.innerHTML = `
        <td><strong>${escapeHtml(row.program_code)}</strong></td>
        <td>${escapeHtml(row.name)}</td>
        <td><span class="badge badge-admin">${escapeHtml(row.category)}</span></td>
        <td><strong>${row.budget_slots}</strong></td>
        <td>${row.total_applications}</td>
        <td><span class="badge badge-approved" style="padding: 1px 6px;">${row.approved_count}</span></td>
        <td><span class="badge badge-released" style="padding: 1px 6px;">${row.released_count}</span></td>
      `;
      body.appendChild(tr);
    });
  }

  // Print execution
  btnPrint.addEventListener('click', () => {
    window.print();
  });
}

// Helpers
function escapeHtml(str) {
  if (!str) return '';
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}
